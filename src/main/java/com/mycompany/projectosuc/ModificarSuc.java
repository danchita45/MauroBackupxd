    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed

        if (VerificarCampos()) {
            lsarchivo archivo = new lsarchivo();
            ListaDoblementeLigada ldl = archivo.SacaDatos();
            
            
            NodoLista nodo = ldl.eliminar((String) ComboSucursal.getSelectedItem());
            Sucursales SucPallenar = (Sucursales) nodo.getTObj();

            SucPallenar.setNoSuc(Integer.parseInt(noSuctxt.getText()));
            SucPallenar.setNombre(nombreSuctxt.getText());
            SucPallenar.setZona(Integer.parseInt(zonatxt.getText()));

            nodo.setEtiqueta(Integer.toString(SucPallenar.getNoSuc()));
            nodo.setTObj(SucPallenar);
            
            
            ldl.inserta(nodo);
            try {
                archivo.InsertarnuevaLista(ldl);
            } catch (IOException ex) {
                Logger.getLogger(AltaSuc.class.getName()).log(Level.SEVERE, null, ex);
            }
            MenuPrinc menu = new MenuPrinc();
            menu.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_GuardarActionPerformed
    public boolean VerificarCampos() {
        if (noSuctxt.getText() != "" && nombreSuctxt.getText() != "" && zonatxt.getText() != "") {
            return true;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Verifique todos los campos ");
            return false;
        }
    }

    public void llenarCombox() {
        ComboSucursal.removeAllItems();
        lsarchivo nuevo = new lsarchivo();
        ListaDoblementeLigada nl = nuevo.SacaDatos();
        NodoLista nodoSuc = nl.getr();
        int i = 0;
        while (i < nl.count()) {
            Sucursales sucr = (Sucursales) nodoSuc.getTObj();
            ComboSucursal.addItem(Integer.toString(sucr.getNoSuc()));
            nodoSuc = nodoSuc.getSig();
            i++;
        }
    }

