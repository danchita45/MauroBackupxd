    private void ComboSucursalItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboSucursalItemStateChanged

        lsarchivo archivo = new lsarchivo();
        ListaDoblementeLigada ldl = archivo.SacaDatos();
        NodoLista nodo = ldl.buscar((String) ComboSucursal.getSelectedItem());
        if (nodo != null) {
            Sucursales SucPallenar = (Sucursales) nodo.getTObj();
            noSuctxt.setText(Integer.toString(SucPallenar.getNoSuc()));
            nombreSuctxt.setText(SucPallenar.getNombre());
            zonatxt.setText(Integer.toString(SucPallenar.getZona()));
        }


    }//GEN-LAST:event_ComboSucursalItemStateChanged

